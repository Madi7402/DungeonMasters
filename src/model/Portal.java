package model;

/**
 * An Enum of Portal types.
 * Portals can be of three types: NONE, ENTRANCE, and EXIT.
 * NONE represents a room that is not a special portal, ENTRANCE represents the entrance portal,
 * and EXIT represents the exit portal.
 *
 * @author Jonathan Abrams, Martha Emerson, Madison Pope
 */
public enum Portal {
    NONE, ENTRANCE, EXIT;
}
